public class Exam03 {
    public static void main(String[] args) {
        //프로그램의 기본 연산자
        int a = 20;
        int b = 10;
        int c = a + b; //더하기 +
        int d = a - b; //나누기 -
        int e = a * b; //곱하기 *
        int f = a / b; //나누기 /
        int g = a % b; //나머지 %
        System.out.println("a + b = " + c);
        System.out.println("a - b = " + d);
        System.out.println("a * b = " + e);
        System.out.println("a / b = " + f);
        System.out.println("a % b = " + g);
    }
}
