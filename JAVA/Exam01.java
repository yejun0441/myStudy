public class Exam01 {
    public static void main(String[] args) {
        // 자바의 변수 타입    
        byte a = 1; // 크기 1byte 최대 127 최소 -128
        short b = 1; // 크기 2byte 최대 32767 최소 -32768
        int c = 1; //  크기 4byte 최대 21억 최소 -21억
        long d = 1; // 크기 8byte 최대 922경 최소 -922경
        float e = 1.5f; // 크기 4byte
        double f = 2.5; // 크기 8byte
        char g = 'a'; // 크기 2byte 유니코드 문자만 저장 가능
        String h = "abc"; // 크기 4byte 이상, 문자열
        boolean i = true; // 크기 1byte true/false만 저장 가능 
    }
}
